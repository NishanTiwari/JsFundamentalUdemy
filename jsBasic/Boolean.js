// Boolean logic 

var firstName = "Ram";
var age = 25;

if (age < 13) {
    console.log(firstName + " is a boy");
}
else if (age >= 13 && age < 16) {
    console.log(firstName + " is a teenager");
}
else if (age >= 20 && age < 30) {
    console.log(firstName + " is a man");
}
else {
    console.log(firstName + " is a youg adult");
}
